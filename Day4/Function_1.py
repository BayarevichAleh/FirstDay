def function_1(num_1, num_2):
    """
    Finding the arithmetic mean
    :param num_1: number 1
    :param num_2: number 2
    :type num_1, num_2: float
    :return: arithmetic mean
    :return type: float
    """
    return (num_1 + num_2) / 2


print(function_1(float(input("Enter number 1:")), float(input("Enter number 2:"))))
